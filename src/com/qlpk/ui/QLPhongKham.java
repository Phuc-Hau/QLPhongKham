    PhongKhamBenhDao daoPKBenh = new PhongKhamBenhDao();
    DTable dTable = new DTable();
    void inti(){
        this.fillTablePhongKham();
        this.updateStatusPhongKham();
        tblPhongKham.setDefaultRenderer(tblPhongKham.getColumnClass(0), dTable);
    }
    
    int row = -1;
    void clearFormPK() {
        txtMaPK.setText("");
        txtGhiChu.setText("");
        txtTenPK.setText("");
        this.row = -1;
        this.updateStatusPhongKham();
    }

    void editPhongKham() {
        String th = (String) tblPhongKham.getValueAt(this.row, 0);
        PhongKham pk = daoPKBenh.selectByID(th);
        this.setFormPhongKham(pk);
        this.updateStatusPhongKham();
    }

    void insertPhongKham() {
        PhongKham th = getFormPhongKham();
        try {
            daoPKBenh.insert(th);
            this.fillTablePhongKham();
            this.clearFormPK();
            Msgbox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            e.printStackTrace();
            Msgbox.alert(this, "Thêm mới thất bại!");
        }
    }

    void updatePhongKham() {
        PhongKham pk = getFormPhongKham();
        try {
            daoPKBenh.update(pk);
            this.fillTablePhongKham();
            Msgbox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            Msgbox.alert(this, "Cập nhật thất bại!");
        }
    }

    void deletePhongKham() {
        String loaiPK = txtMaPK.getText();
        if (Msgbox.confirm(this, "Bạn thực sự muốn xóa phòng "+loaiPK+ " này?")) {
            try {
                daoPKBenh.detele(loaiPK);
                this.fillTablePhongKham();
                this.clearFormPK();
                Msgbox.alert(this, "Xoá thành công!");
            } catch (Exception e) {
                Msgbox.alert(this, "Xóa thất bại!");
            }
        }
    }

    //Đỗ dữ liệu vào tblphongkham
    void fillTablePhongKham() {
        DefaultTableModel model = (DefaultTableModel) tblPhongKham.getModel();
        model.setRowCount(0);
        try {
            List<PhongKham> list = daoPKBenh.selectAll();
            for (PhongKham pk : list) {
                Object[] row = {pk.getMaPK(), pk.getTenPhongKham(), pk.getGhiChu()};
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            Msgbox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    //Hien thi thuoc len form
    void setFormPhongKham(PhongKham pk) {
        txtMaPK.setText(pk.getMaPK());
        txtTenPK.setText(pk.getTenPhongKham());
        txtGhiChu.setText(String.valueOf(pk.getGhiChu()));
    }

    //tao thuoc tu form
    PhongKham getFormPhongKham() {
        PhongKham pk = new PhongKham();
        pk.setMaPK(txtMaPK.getText().toUpperCase());
        pk.setTenPhongKham(txtTenPK.getText());
        pk.setGhiChu(txtGhiChu.getText());
        return pk;
    }

    //cap nhap trang thai cac nut
    void updateStatusPhongKham() {
        boolean edit = (this.row >= 0);
        //Trang thai form
        txtMaPK.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
    }
    
    boolean ErrorPhongKham() {
        if (Utility.checkNullText(txtTenPK) && Utility.checkNullText(txtMaPK) ) {
            return true;
        } else {
            Msgbox.alert(this, "Không bỏ trống thông tin");
            return false;
        }
    }
