    void uiti(){
        Thread t = new Thread(){
            @Override
            public void run() {
                String txt = lblTile.getText()+"             ";
                while (true) {                    
                    txt = txt.substring(1,txt.length())+txt.charAt(0);
                    try {
                        sleep(300);
                    } catch (Exception e) {
                        e.printStackTrace();
                    } 
                    lblTile.setText(txt);
                }
            }
            
        };
        t.start(); 
    }
    
